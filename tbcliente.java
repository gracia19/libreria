public void cargatabla(){
    DefaultTableModel tabla=new DefaultTableModel ();
        tabla.addColumn("Documento  ");
        tabla.addColumn("Nombre Cliente  ");
        tabla.addColumn("Apellido  ");
        tabla.addColumn("Direccion  ");
        tabla.addColumn("Departamento  ");
        tabla.addColumn("Mes cumplea√±os  ");
        tabla.addColumn("Clave  ");
        cliente.setModel(tabla);
        String datos[]=new String[7];
        
        try{
         op=cnn.prepareStatement("SELECT *FROM tbCliente");
         rs=op.executeQuery();
         while (rs.next()){
           datos[0]=rs.getString(1);
           datos[1]=rs.getString(2);
           datos[2]=rs.getString(3);
           datos[3]=rs.getString(4);
           datos[4]=rs.getString(5);
           datos[5]=rs.getString(6);
           datos[6]=rs.getString(7);
           tabla.addRow(datos);
         }
        } catch (Exception e) {
            System.out.print(e);
        }
}
